package org.cis120.chinesechess;

/**
 * This file holds an enumeration called GameState, which is used to
 * indicate the current state of the ChineseChess game.
 *
 */
public enum GameState {
    RED_CHECKMATE,
    BLACK_CHECKMATE,
    RED_CHECK,
    BLACK_CHECK,
    BEGIN_MOVE_MODE,
    END_MOVE_MODE1,
    END_MOVE_MODE2
}
